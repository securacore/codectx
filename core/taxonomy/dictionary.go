// DictionaryLookup returns the synonym list for a normalized term key.
// Returns nil if the term is not in the dictionary.
func DictionaryLookup(key string) []string {
	return technicalDictionary[key]
}

// dictionarySize returns the number of entries in the technical dictionary.
func dictionarySize() int {
	return len(technicalDictionary)
}

// applyDictionary adds dictionary-sourced aliases to existing taxonomy terms.
// For each term in the taxonomy, if the dictionary has synonyms for that
// term's key, those synonyms are added as aliases.
func applyDictionary(terms map[string]*Term) {
	for key, term := range terms {
		synonyms := technicalDictionary[key]
		for _, syn := range synonyms {
			addAlias(term, syn)
		}

		// Also check if the canonical form (lowercased) yields different
		// dictionary matches than the slug key.
		canonicalKey := NormalizeKey(term.Canonical)
		if canonicalKey != key {
			for _, syn := range technicalDictionary[canonicalKey] {
				addAlias(term, syn)
			}
		}
	}
}
